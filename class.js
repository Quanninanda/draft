class Product{
    name;
    capacity;
    price;
    quantity;
    img;
    total;
    detail;
    constructor(name, capacity, price, quantity, img, total, detail) {
        this.name = name;
        this.capacity = capacity;
        this.price = price;
        this.quantity = quantity;
        this.img = img;
        this.total = total;
        this.detail = detail;
    }
}